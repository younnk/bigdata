# 퀴즈 프로그램

# 문제 데이터베이스
문제들 <- list(
  list(
    문제 = "mpg 변수의 제 1사분위수를 구하고 반올림하여 정수값으로 출력하시오.",
    정답 = "round(quantile(df$mpg, 0.25))",
    힌트 = "quantile() 함수를 사용하여 1사분위수를 구하고, round() 함수로 반올림하세요."
  ),
  list(
    문제 = "mpg 값이 19이상 21이하인 데이터의 수를 구하시오.",
    정답 = "sum(df$mpg >= 19 & df$mpg <= 21)",
    힌트 = "조건에 맞는 데이터를 논리값으로 변환한 후 sum() 함수를 사용하세요."
  )
  # 여기에 더 많은 문제를 추가할 수 있습니다.
)

# 퀴즈 실행 함수
퀴즈_실행 <- function() {
  for (문제 in 문제들) {
    cat("\n", 문제$문제, "\n")
    
    while (TRUE) {
      cat("코드를 입력하세요 (힌트를 원하면 'hint' 입력): ")
      사용자_입력 <- readline()
      
      if (사용자_입력 == "hint") {
        cat("힌트:", 문제$힌트, "\n")
      } else if (사용자_입력 == 문제$정답) {
        cat("정답입니다!\n")
        break
      } else {
        cat("오답입니다. 다시 시도해보세요.\n")
      }
    }
  }
  
  cat("\n모든 문제를 완료했습니다!\n")
}

# 퀴즈 실행
퀴즈_실행()
